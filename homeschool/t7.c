//7번 문제
//사용자가 입력한 정수 n에 대해 2^k >= n을 만족하는 정수

// 2의 거듭제곱이 n보다 크거나 같은 최소의 k를 찾는 문제
#define _CRT_SECURE_NO_WARNINGS
#include<stdio.h>

int main(void) {
    int n;  //사용자로부터 입력받을 정수 n
    int k = 0; //조건을 만족하는 최소의 k 값 (초기값은 0부터 시작)
    int value = 1; // 2^k 값을 저장할 변수 (2^0 = 1부터 시작)

    // 사용자에게 n을 입력받는다
    printf("정수 n 입력: ");
    scanf("%d", &n);    // 사용자가 256 입력

    // 반복문을 사용하여 2^k >= n을 만족하는 최소 k를 찾는다
    while (value < n) {
        value *= 2; // 현재 value에 2를 곱함: 2^k 계산
        k++;        // 곱셈이 한 번 일어날 때마다 k를 1씩 증가

        // 예: n이 256이면
        // value = 1, k = 0 → 1 < 256 → 반복
        // value = 2, k = 1 → 2 < 256 → 반복
        // value = 4, k = 2 → 4 < 256 → 반복
        // ...
        // value = 256, k = 8 → 256 == 256 → 조건 만족 → 반복 종료
    }
    
    printf("공식을 만족하는 k의 최댓값은: %d\n", k);

    return 0;   //프로그램 종류
}